        public void createTPDefaultRecipeXML()
        {
            string pathOld = @"C:\Users\jicwang\Documents\Study\Code\CSharp\CSharpStudy\CSharpStudy\CSharpStudy\Resources\ThermaProbe_ConfigurationSetting.xml";
            string pathNew = @"C:\Users\jicwang\Documents\Study\Code\CSharp\CSharpStudy\CSharpStudy\CSharpStudy\Resources\DefaultRecipeTP.xml";
            XDocument xdoc = XDocument.Load(pathOld);
            XDocument xdocNew = XDocument.Load(pathNew);
            XElement xElementNew = xdocNew.Element("Recipe").Element("JobSetup");
            using (StreamWriter sw = new StreamWriter("log.txt"))
            {
                //foreach (var xmlName in m_RecipeIDList)
                for(int i=0; i< m_RecipeIDList.Length; i++)
                {
                    string xmlName = m_RecipeIDList[i];
                    IEnumerable<XElement> sql = from item in xdoc.Descendants("Item").Where(p => p.Element("Name").Value == xmlName) select item;
                    if (sql != null)
                    {
                        foreach (var item in sql)
                        {
                            xElementNew.Add(item);
                            Console.WriteLine(string.Format("update {0}", xmlName));
                            sw.WriteLine(xmlName);
                        }
                    }
                    else
                    {
                        sw.WriteLine(string.Format("missing {0}", xmlName));
                        Console.WriteLine(string.Format("can't find {0}", xmlName));
                    }
                }
            }
            xdoc.Save(pathOld);
            xdocNew.Save(pathNew);
        }

        public void orderXML()
        {
            string pathOld = @"C:\Users\jicwang\Documents\Study\Code\CSharp\CSharpStudy\CSharpStudy\CSharpStudy\Resources\DefaultRecipeTP.xml";

            XDocument xdoc = XDocument.Load(pathOld);
            XElement xElementNew = xdoc.Element("Recipe").Element("JobSetup");

            var orderedList = xElementNew.Elements("Item").OrderBy(p => (int)p.Element("Index")).ToArray();
            xElementNew.RemoveAll();
            foreach (var item in orderedList)
            {
                xElementNew.Add(item);
            }

            xdoc.Save(pathOld);

            Console.WriteLine("done!");
        }

